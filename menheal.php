		<h1>Why mental health is important?</h1>
			Mental health includes our emotional, psychological, and social well-being. It affects how we think, feel, and act. <br>It also helps determine how we handle stress, relate to others, and make choices. Mental health is important at every stage of life, from childhood and adolescence through adulthood.
		<h3>Why is emotional health important?</h3>
			<p>Emotional and mental health is important because it’s a vital part of your life and impacts your thoughts, behaviors and emotions. <br>Being healthy emotionally can promote productivity and effectiveness in activities like work, school or caregiving. <br>It plays an important part in the health of your relationships, and allows you to adapt to changes in your life and cope with adversity.</p>
		<h3>How can you improve your emotional health day-to-day?</h3>
			There are steps you can take to improve your mental health everyday. Small things like exercising, eating a balanced and healthy meals, opening up to other people in your life, taking a break when you need to, remembering something you are grateful for and getting a good night’s sleep, can be helpful in boosting your emotional health.			
		<h3>When is a good time to reach out for help?</h3>
			Issues related to mental health can impact different people in different ways. If you start to see changes in your overall happiness and relationships, there are always ways get the support you want. 
		<h4>Here are some ways you can get help:</h4>
			Connect with other individuals, friends and family — Reaching out and opening up to other people in your life can help provide emotional support
			Learn more about mental health — There are many resources you can turn to for learning more about emotional health.
