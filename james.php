				<p>In recent years I had the feeling that something was very off with me. I would have panic attacks and meltdowns lasting up to several hours. I always knew I had a learning disability, anxiety, and a specific phobia. Sensory Issues such as tastes, smells, textures, certain smells, and sights bothered me but I never mentioned it to a professional. I detested staying at someone's house or at a hotel and even had trouble feeling comfortable in my own home.</p><br><br>

				<p>When I saw the show Atypical it gave me an idea to do a little research. I knew autism was different for everyone but I could see similarities between the main character of the show and myself. Learning more about ASD became a new obsession for me. I then decided to take the plunge and see a specialist. I was told by the doctor that performed the screening that they were pleasantly surprised at how functional I seemed compared to my test results. It was indeed a compliment but it had me thinking a lot about my life in both the past and the present. I was in tears because of the mixed feelings about my life.</p> <br><br>

