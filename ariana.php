 			 <figcaption><br><hr><br><p>Looking back, I can see that I had symptoms of an anxiety disorder even as a small child. I remember going for weeks at a time waking up, unable to go back to sleep. Then, as if by magic, I would go back to sleeping normally.</p><br><br>

			<p>I remember being scared to death that I would die in my sleep. I feared death, what was beyond death—I was scared stiff. When I was 14 years old, I experienced my first panic attack. I thought I was dying, or at the very least gravely ill. The doctors didn’t recognize it as a panic attack. They tried treating my physical symptoms, ignoring the obvious psychological situation. I did not help much by insisting that my problem was not stress-related. I took medication to control my erratic, rapid heart rate. Now I know that was a pre-panic symptom.</p><br><br>

			<p>Four years later, I began having trouble breathing. My mother and I dismissed it as stress about going away to college, and we figured it would go away with time. After several months of being frequently short of breath and even dizzy at times, I visited a doctor. My problems were finally recognized as psychological. At last my problem was being treated, and I started medication designed to treat it.</p><br><br>

			<p>I have been on a few different medications since then, and I am glad to say that I am doing very well with my current treatment. It took a long time for me to accept that just like any other chronic illness, panic disorder is something I must live with. By talking to a counselor, I learned to accept that I require medication, much like a diabetic who needs insulin. It is simply a part of my life.</p><br><br>

			<p>I still have panic attacks from time to time, and my life is far from perfect. But I am much happier; I laugh more and I’m more comfortable spending time with other people, and I have a much greater quality of life. I’ve learned how to deal with my panic attacks and to realize when one is coming on.</p><br><br>

			<p>I feel it is my responsibility to share my story with others because panic disorder is still vastly misunderstood. When people like me are unwilling to speak up, we lessen our chances for happiness. I am able to live a normal life today because others were willing to speak up. I want to do the same thing for others.</p><br><br>

