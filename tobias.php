			<h1 align="center"><i><u>My Lifelong Struggle With Social Anxiety  </u></i><br>  By Tobias Atkins</h1>

			<figure>
 			 <img src="images/tobias.jpeg" alt="anxiety" style="width:100%"><br>
 			 <figcaption><br><hr><br><p>For most of my life I’ve struggled with social anxiety disorder, along with generalized anxiety disorder, OCD, and depression. During the worst of it, I was on strong medication and medical disability benefits due to my fear of job interviews. I would feel uncomfortable or awkward in public 90 percent of the time. I found it difficult to relax and be myself around people, even with friends that I have known for years.</p><br><br>

			<p>I am sharing my story of healing to let you know it can get better, as long as you do the work.</p><br><br>

			<p><b>Born Shy and Sensitive</b></p><br><br>
			<p>I was always a shy and sensitive kid, but I didn’t even know what social anxiety disorder was until I went to see my first psychologist. I believed I was born shy, and there was nothing I could do to fix it, so I didn’t even try. I had given up on myself, convinced I didn't win the genetic lottery, so better luck next lifetime.</p><br><br>

			<p>For years I suffered in silence. I felt so embarrassed and ashamed t that I didn’t tell anyone or seek help. I was raised to believe it was weak to talk about feelings. When I did try to tell people, I felt they didn’t understand me or what I was going through. I was told things like “harden up” and “who cares what others think?” I have since come to realize that admitting you need help and talking about your feelings is one the bravest things you can do.</p><br><br>

			<p>Asking for help is the first step to change: If you act like everything’s all right when it’s not, how do you expect anyone to be able to help you? Statistics show that most people suffer in silence for around 10 years before seeking help with social anxiety. That is crazy because this condition is treatable.</p><br><br>

			<p><b>How Everything Changed</b></p><br><br>
			<p>My life changed when I sought help. I went to see psychologists and I met a former social anxiety sufferer who, through a lot of effort on his part, managed to persuade me that social anxiety was not a life sentence. Once I believed that I could overcome social anxiety, everything changed for me.</p><br><br>

			<p>The psychologists taught me to stop calling myself hurtful names like loser, ugly, and weird. They taught me to accept myself for all of who I am, good points and bad. They taught me that nobody's perfect, but that’s OK because I don’t need to be perfect or never appear awkward for others to like and accept me.</p><br><br>

			<p>Instead of always looking for others to approve of who I am, they taught me to look for my own feelings of self-worth and validation. When that happened, all of a sudden what other people thought of me didn’t seem to matter that much anymore. I became more concerned with how I felt about me.</p><br><br>

			<p><b>Tools and Knowledge</b></p><br><br>
			<p>If you are currently going through something similar, I really encourage you to go and see a psychologist. They have tools and knowledge that can help you overcome this condition. For example, my psychologist introduced me to a technique called EFT (emotional freedom techniques). EFT has been instrumental in helping me get over social anxiety. But there are many other therapies that have helped people with social anxiety.</p><br><br>

			<p>I am now paying it forward by teaching others what I have learned on my journey to healing social anxiety and shyness. My mission is to let people know social anxiety is not a life sentence and with some work, you can get over it too.</p><br><br>

			<p>Tobias Atkins is an Australian author and proud introvert whose self-improvement articles have been featured on popular blogs such as Lifehack and Pick the Brain. He enjoys helping others by sharing everything he learned on his long road to recovery, which you can read in his book How I Overcame Social Anxiety.</p><br><br>

