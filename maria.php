			<h1 align="center"><i><u>I'm a Comedian, And I'm Depressed </u></i><br>  By Maria Pierce</h1>

			<figure>
 			 <img src="images/maria.jpg" alt="anxiety" style="width:100%"><br>
 			 <figcaption><br><hr><br><p>Ever wonder what depression feels like? Here’s a hint: Take a pillowcase full of rocks and strap it to the top of your head. Now put on a dark pair of sunglasses — indoors. Leave those things on for about a week. Until you begin to see the world through a dark film that never gets lighter, and it takes a very conscious effort to hold your head up. That is what depression feels like on a good day.</p><br><br>

			<p>And that’s the way I describe my depression to thousands of people in concerts all across America. You see, I’m a comedian. And I’m depressed.</p><br><br>

			<p>Oh, believe me, depression, like most diseases — unchecked, uncared for — can progress and become something so devastating that you are absolutely convinced there is no reason for you to be alive. Yes, I’ve been that depressed, too. And here’s the kicker: I am a born-again Christian who makes a living making people laugh!</p><br><br>

			<p><b>Growing Up In the Church</b></p><br><br>
			<p>I grew up in a strict Christian home. I was a preacher’s daughter and part of a church culture that taught most often that if you were depressed it simply meant there was some great spiritual flaw in your life. To that thinking I say in concert often, “Baloney!”</p><br><br>

			<p>Not long after beginning medicine to level my brain chemistry, help me sleep, and escape the dreadful negative voices in my head, a woman came to me after a show and said, “I don’t think you should talk about taking antidepressants. It makes your faith look weak!” I said to her very candidly, “Then you should take your glasses off and drive home! Where’s your faith?”</p><br><br>

			<p>Over the last 15 years I have seen great changes in the way the church world handles mental illness. And there is no one happier about that than me. There are researchers, doctors, and pharmacists — many who are devoted Christians who understand that our brains are simply another important organ in the body, and they all work feverishly to find the right cure, medication, and help for a variety of diseases. The church is not the only judgmental group out there when it comes to understanding mental illness. There are other religions and cultures that look on mental illness as something you should be able to overcome by shear will.</p><br><br>

			<p><b>The Ridiculous Few</b></p><br><br>
			<p>You would never wag a finger in the face of a diabetic and shame them for taking insulin. Nor should you look upon a homeless man and tell him to get up and go to his house and stop talking to himself. Until we begin to see that mental illness attacks an organ in our body that needs medical attention, therapy, and understanding, we will still run into the ridiculous few who see those in a wheelchair as lazy. To those people…  well, they become the best part of my night: material for fodder, jokes for a comedian. But sadly, they are rarely worth the laugh.</p><br><br>

 

