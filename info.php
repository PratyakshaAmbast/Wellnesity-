		<h1>Eating Disorder</h1>
		<p>There is a commonly held misconception that eating disorders are a lifestyle choice.<br> Eating disorders are actually serious and often fatal illnesses that are associated with severe disturbances in people’s eating behaviors and related thoughts and emotions.</p><br>
		<p><u>Signs and Symptons</u></p><br>
		<p>Extremely restricted eating<br>
		Extreme thinness (emaciation)<br>
		A relentless pursuit of thinness and unwillingness to maintain a normal or healthy weight<br>
		Intense fear of gaining weight<br>
		Distorted body image, a self-esteem that is heavily influenced by perceptions of body weight and shape, or a denial of the seriousness of low body weight</p><br>
		<p><u>Treatment and Therapies</u></p>
		<p>It is important to seek treatment early for eating disorders. People with eating disorders are at higher risk for suicide and medical complications.<br> People with eating disorders can often have other mental disorders (such as depression or anxiety) or problems with substance use.<br> Complete recovery is possible.<br>
			Treatment plans are tailored to individual needs and may include one or more of the following:<br>
			Individual, group, and/or family psychotherapy<br>
			Medical care and monitoring<br>
			Nutritional counseling<br>
			Medications</p>
