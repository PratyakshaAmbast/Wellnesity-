		<section class ="infor">
		<h1>Depression</h1>
		<p>Depression (major depressive disorder or clinical depression) is a common but serious mood disorder. </br>It causes severe symptoms that affect how you feel, think, and handle daily activities, such as sleeping, eating, or working</p><br>
		<p>1.<u>Persistent depressive</u> disorder (also called dysthymia) is a depressed mood that lasts for at least two years. A person diagnosed with persistent depressive disorder may have episodes of major depression along with periods of less severe symptoms, but symptoms must last for two years to be considered persistent depressive disorder.</p>
		</br><p>2.<u>Postpartum depression</u> is much more serious than the “baby blues” (relatively mild depressive and anxiety symptoms that typically clear within two weeks after delivery) that many women experience after giving birth. Women with postpartum depression experience full-blown major depression during pregnancy or after delivery (postpartum depression). The feelings of extreme sadness, anxiety, and exhaustion that accompany postpartum depression may make it difficult for these new mothers to complete daily care activities for themselves and/or for their babies.
		</br><p>3.<u>Psychotic depression</u> occurs when a person has severe depression plus some form of psychosis, such as having disturbing false fixed beliefs (delusions) or hearing or seeing upsetting things that others cannot hear or see (hallucinations). The psychotic symptoms typically have a depressive “theme,” such as delusions of guilt, poverty, or illness.
		</br><p>4.<u>Seasonal affective disorder</u> is characterized by the onset of depression during the winter months, when there is less natural sunlight. This depression generally lifts during spring and summer. Winter depression, typically accompanied by social withdrawal, increased sleep, and weight gain, predictably returns every year in seasonal affective disorder.
		</br><p>5.<u>Bipolar disorder</u> is different from depression, but it is included in this list is because someone with bipolar disorder experiences episodes of extremely low moods that meet the criteria for major depression (called “bipolar depression”). But a person with bipolar disorder also experiences extreme high – euphoric or irritable – moods called “mania” or a less severe form called “hypomania.”</p>
		</br><br><p><strong><u>Signs and Symptoms</u></strong></p>
		</p>If you have been experiencing some of the following signs and symptoms most of the day, nearly every day, for at least two weeks, you may be suffering from depression:</p>

			<P>*Persistent sad, anxious, or “empty” mood<br>
			*Feelings of hopelessness, or pessimism<br>
			*Irritability<br>
			*Feelings of guilt, worthlessness, or helplessness<br>
			*Loss of interest or pleasure in hobbies and activities<br>
			*Decreased energy or fatigue<br>
			*Moving or talking more slowly<br>
			*Feeling restless or having trouble sitting still</p>
			<br>
			<p><strong><u>Treatment and Therapies</u></strong></p>
			<p>Depression, even the most severe cases, can be treated. The earlier that treatment can begin, the more effective it is. Depression is usually treated with medications, psychotherapy, or a combination of the two. If these treatments do not reduce symptoms, electroconvulsive therapy (ECT) and other brain stimulation therapies may be options to explore.</p>
