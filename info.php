		<h2> Anxiety Disorder</h2></br>
		<p>Occasional anxiety is an expected part of life.</br>The symptoms can interfere with daily activities such as job performance, school work, and relationships.</p>
		</br>
		<h1>Generalized Anxiety Disorder</h1>
		<p>People with generalized anxiety disorder (GAD) display excessive anxiety or worry, most days for at least 6 months, about a number of things such as personal health, work, social interactions, and everyday routine life circumstances.</p>
		<br>
		<p><u>Generalized anxiety disorder symptoms include:</u></p>
			<p>*Feeling restless, wound-up, or on-edge</br>
			*Being easily fatigued</br>
			*Having difficulty concentrating; mind going blank</br>
			*Being irritable</br>
			*Having muscle tension</br>
			*Difficulty controlling feelings of worry</br>
			*Having sleep problems, such as difficulty falling or staying asleep, restlessness, or unsatisfying sleep</p>
		<br>
		<p><strong><u>Treatment & Therapy</u></strong></p>
		<p>Anxiety disorders are generally treated with psychotherapy, medication, or both. </br>There are many ways to treat anxiety and people should work with their doctor to choose the treatment that is best for them.</p>
		<p>Psychotherapy
		<p>Psychotherapy or “talk therapy” can help people with anxiety disorders. To be effective, psychotherapy must be directed at the person’s specific anxieties and tailored to his or her needs.</p>
		
