		<h1>Obsessive-Compulsive Disorder(OCD)</h1>
		<p>It  is a common, chronic, and long-lasting disorder in which a person has uncontrollable, reoccurring thoughts (obsessions) and/or behaviors (compulsions) that he or she feels the urge to repeat over and over.</p>
		<br>
		<p><u>Signs and Symptoms</u></p>
		<p><b>Obsessions</b> are repeated thoughts, urges, or mental images that cause anxiety. </br>Common symptoms include:<br>
			Fear of germs or contamination<br>
			Unwanted forbidden or taboo thoughts involving sex, religion, or harm<br>
			Aggressive thoughts towards others or self<br>
			Having things symmetrical or in a perfect order</p><br>
		<p><b>Compulsions </b>are repetitive behaviors that a person with OCD feels the urge to do in response to an obsessive thought. <>Common compulsions include:
			Excessive cleaning and/or handwashing<br>
			Ordering and arranging things in a particular, precise way<br>
			Repeatedly checking on things, such as repeatedly checking to see if the door is locked or that the oven is off<br>
			Compulsive counting</p>
			<br>
			<p><u>Treatments and Therapies</u></p>
				OCD is typically treated with medication, psychotherapy, or a combination of the two.<br> Although most patients with OCD respond to treatment, some patients continue to experience symptoms.<br>
				Sometimes people with OCD also have other mental disorders, such as anxiety, depression, and body dysmorphic disorder, a disorder in which someone mistakenly believes that a part of their body is abnormal. <br>It is important to consider these other disorders when making decisions about treatment.</p>
		</section>
