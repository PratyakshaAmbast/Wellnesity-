		<section class="infor">
		<h2>Post-Traumatic Stress Disorder(PTSD)</h2>
		<p>It is a disorder that develops in some people who have experienced a shocking, scary, or dangerous event.</p></br>
		<p><u>Signs and Symptoms</u></p><br>
		<p>To be diagnosed with PTSD, an adult must have all of the following for at least 1 month:<br>
			At least one re-experiencing symptom<br>
			At least one avoidance symptom<br>
			At least two arousal and reactivity symptoms<br>
			At least two cognition and mood symptoms</p><br>
		<p><u>Therapies and Treatments</u></p>
		<p>The main treatments for people with PTSD are medications, psychotherapy (“talk” therapy), or both. Everyone is different, and PTSD affects people differently, so a treatment that works for one person may not work for another. <br>It is important for anyone with PTSD to be treated by a mental health provider who is experienced with PTSD.<br> Some people with PTSD may need to try different treatments to find what works for their symptoms.
		<br><br>
		If someone with PTSD is going through an ongoing trauma, such as being in an abusive relationship, both of the problems need to be addressed. <br>Other ongoing problems can include panic disorder, depression, substance abuse, and feeling suicidal.<p>
		</section>
