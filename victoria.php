			<p>My relief was to be short-lived, however, as a man I loved very much committed suicide by hanging eight months later, and I was the one to find him. For the next six months my life went into a tailspin of intense pain and sorrow over his death, complicated by the divorce I was going through at the time. The posttraumatic stress disorder (PTSD) I suffered as a result of his death took over my life, and it became almost impossible for me to function. Brushing my teeth, combing my hair, taking a shower, or fixing meals were almost insurmountable tasks most days. Gradually it got better and I was able to leave my apartment. I started doing things with friends again, but I still felt safest at home.<p><br><br>

			<p>Thanks to a very loving and understanding partner, I was able to work through more of the anxiety I felt and start living a somewhat "normal" life. This relationship was to last for a year and a half and it was instrumental in helping to bring me out of the shadows.<p><br><br>

			<p>This fall I enrolled in college again and began work on a second undergraduate degree, double majoring in multimedia/web design and computer graphic design. I still have difficulty concentrating, an exaggerated startle response, and problems with feeling worthless at times. I find myself becoming irritable when I don't live up to my own expectations and angry about being afraid to develop a new support network of friends. I am fortunate, though, to have a very large support network of online friends who are also survivors of a loved one's suicide. This group has been very important in keeping me alive for the past three years. I also have a psychotherapist I respect and admire, who works hard with me to unlock the mysteries of my mind.<p><br><br>

			<p>I have been blessed with two sons and a daughter-in-law who are the most precious people in my life, and their belief in my ability to succeed is a driving force. Even though they may not completely understand the nature of my mental illnesses, my loving family has been an immense source of support in my struggles to redefine my life and become the person I want to be.<p><br><br>

			<p>I still wish it were easier living with mental illness, but I know I have some responsibility in making that happen. I could be more compliant with my medication regimen than I am at times. I need to develop better eating habits and sleeping schedules. And I need to avoid sabotaging myself by inviting people into my life that distract me from the things that are most important. I am a caretaker by nature and have a tendency to overextend myself when I find myself in a situation where there is another person in my life whom I feel needs nurturing.<p><br><br>

			<p>Life is an ever-changing process of growing and learning. Each day I learn more about who I am and what I want from my life. On most days I feel I am capable of making my dreams come true. I don't want to be defined as a mentally ill person. I am a person who lives with mental illness and functions to the best of my ability. Living with mental illness has its challenges, and I want to keep meeting them head-on.<p><br><br>

 

