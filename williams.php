			<p>At the age of five I started playing baseball. As I got older it became clear I was very gifted. I saw the joy that my family and peers got when I played well, and for the first time I found a place where I wasn't scared and didn't feel inferior. I didn't understand that my self-worth shouldn’t depend on other people. Baseball was my key to happiness and making people like me. I had friends, my family was happy, and everything was great as long as baseball was going well.</p><br><br>

			<p><b>Happiness...Gone</b></p><br><br>
			<p>Going into my senior year of high school, I was highly recruited to play college baseball. But in December of that year I suffered a knee injury that ended my baseball career.</p><br><br>

 
			<p>My key to happiness was gone. The feelings I had covered up for years returned — and much worse than ever. I turned to alcohol, even though I didn't really enjoy drinking. I stayed in my room with no lights on for days at a time. Sometimes I slept for three days, sometimes 18 hours a day. During this period my grandfather passed away, my best friend was sent to jail, and I saw no hope of ever having a meaningful life. I reached the point of feeling that everyone would be better off, including myself, if I wasn't around anymore. I tried to end my life with alcohol and prescription drugs.</p><br><br>

			<p><b>Rebuilding Myself</b></p><br><br>
			<p>Thankfully, I chose to get help through a counselor. I was diagnosed with generalized anxiety disorder (GAD) and panic attacks. I discovered that my feelings were coming from a subconscious thought of always expecting a crisis to occur. I was preparing my body for some sort of emotional trauma, which caused a problem with my fight-or flight-response. Once I learned to rationally look at my surroundings and myself, I became capable of controlling my reactions to my negative emotions.
			And I began the process of rebuilding myself by taking up golf. And I decided that being a golf coach would be my platform to reach people. My first golf coach, the late Barry McCann, had told me that I had a special gift and I should go after my dream of being a PGA tour coach.  I moved to Orlando, Florida, to learn from Sean Foley, a coach I admired more than anybody. He has become a great friend and mentor and he has taught me many lessons to help me understand that my thinking about myself was the key to everything. The well-known sports coach Paul Dewland has also helped me understand how thinking creates feeling.</p><br><br>

			<p>Golf is my platform to tell this story. No matter how bad things seem, you were meant to win. With faith, people who care, professional help, and the desire to change, anything is possible. Take it from someone who once no longer wanted to live and who now loves every single day.</p><br><br>

