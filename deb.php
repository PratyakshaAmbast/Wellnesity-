			<p>My symptoms came on suddenly after starting the birth control pill with estrogen. Falling into despair I didn’t know what hit me. I was scared and doctors at the time couldn’t explain why either. I saw a psychiatrist, but did not want antidepressants. Instead I engaged in exercise to raise my endorphins, which gave some relief from the depression but not the anxiety. </p><br><br>

			<p>I struggled with anxiety for years. When I gave birth to my first child my anxiety increased even more and I suffered postpartum depression. I tried to manage it with exercise again to avoid psychiatric drugs. My anxiety was more intense than the depression and developed into OCD behaviors, gradually driving me into adrenal fatigue with my second pregnancy five years later. The physical changes were draining and my emotional stress was so heightened I finally resorted to an antidepressant that seemed to aggravate my symptoms. Traditional medical and mental health doctors were unaware that I was estrogen dominant and my body becoming copper toxic as estrogen was retaining copper causing a build up. The excess copper with no where to go disrupted my neurotransmitters activity. I gave it my best fight but in the end I couldn’t go through with my second pregnancy. The trauma of the choice and the procedure added guilt and shame worsening my mental health symptoms and eventually I gave up my career as well. The following eight years involved enduring varying dosages of different antidepressants each with their own awful side effect. They did little to nothing for my depression, worsened my anxiety with a racing mind and panic attacks while at the same time I felt drained and numbed. My loved ones did not know how to help because I had always been a strong, professional working woman seemingly able to handle anything. As a barely functioning depressive person, I took matters into my own hands and went off of the antidepressants to feel again. </p><br><br>

			<p>I definitely got my feelings back. The only problem was they were angry, unhappy emotions surfacing from what I had been through. The love for my daughter helped me cope and keep fighting to be healthy. Then as I entered menopause with unbearable night sweats and took hormone replacement drugs with estrogen my depression became more intense. Not wanting to go back to the awful feeling on antidepressants I decided to try a new approach. I sought doctors who use a natural method of correcting imbalances in neurotransmitter activity with advanced nutrient therapy. This was the best decision of my life. These doctors uncovered that all my hormone related psychiatric symptoms were because my body did not detox copper properly. It took some time to get the excess copper out of my body and balance other depleted nutrients but I started to progressively get better and heal. I can happily say I no longer suffer fatigue, depression, nor OCD, have minimal anxiety and best of all no side effects. All along I had a treatable condition that doesn’t respond to psychiatric medication. Speaking out about what I went through is much easier now that I survived copper toxicity and have peace of mind.</p><br><br>

			<p>Many years ago, I had a boss that nicknamed me “Smiley.” Thanks to advanced nutrient therapy normalizing my brain chemistry, eating a low copper diet and incorporating my own personal healing practices my life has been renewed and I can smile again. For me stopping the roller coaster trial and error of antidepressants and going a natural route was a good decision. The lesson from my journey that I share is that we all have a unique biochemistry and what is right for one may not be for another.</p><br><br>

